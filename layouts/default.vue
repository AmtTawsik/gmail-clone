import { onMounted } from 'vue'
import { Drawer, initFlowbite } from 'flowbite'


const isSending = ref(false);
const isShowToast = ref(false);

const handelSending = (data) => {
    isSending.value = data
    setTimeout(() => {
        isSending.value = false;
    }, 1500);
}
const handelShowToast = (data) => {
    isShowToast.value = data
    setTimeout(() => {
        isShowToast.value = false;
    }, 5000);
}

const handelCloseToast = () => {
    isShowToast.value = false
}


// initialize components based on data attribute selectors
onMounted(() => {
    initFlowbite();
    const $buttonElement = document.querySelector("#button");
    const $drawerElement = document.querySelector("#sidebar");
    const $closeButtons = document.querySelectorAll("#closeButton");

    // set drawer options
    const drawerOptions = {
        backdropClasses:
            "bg-gray-900 bg-opacity-50 dark:bg-opacity-80 fixed inset-0 z-40",
    };

    // create a new drawer instance
    if ($drawerElement) {
        const drawer = new Drawer($drawerElement, drawerOptions);

        // set event listeners for the button to show the drawer
        $buttonElement.addEventListener("click", () => drawer.toggle());
        $closeButtons.forEach((el) => {
            el.addEventListener("click", () => drawer.hide());
        });
    }
})

const isBigDevice = computed(() => {
    if (window.innerWidth < 768) {
        return true;
    } else {
        return false;
    }
});
</script>

